package day2;

public class Calculator {
	
	public  int add(int x, int y, int z) {
		int d = x+y+z ;
		return	d;
	}
		public  int sub(int p, int q) {
			int r = p-q ;
			return 	r;
		}
			public  double mul(double a, double b) {
				double c = a*b ;	
				return c;
			}
				
				public  float div(float i, float j) {
					float k = i/j ;	
					return k;
				}

}
